function d=getData_EsHv();

% Meshdata
data.boundtype=[-1 -1 -1 -1];
data.Coordinates=[0 0; 1 0; 1 1; 0 1];
data.Elements=[1 2 4;2 3 4];

EX1=@(x,a) exp(2/a*(x-1));
DEX1=@(x,a) 2/a*exp(2/a*(x-1));
DDEX1=@(x,a) 2/a*2/a*exp(2/a*(x-1));
DDDEX1=@(x,a) 2/a*2/a*2/a*exp(2/a*(x-1));
EX2=@(x,a) exp(3/a*(x-1));
DEX2=@(x,a) 3/a*exp(3/a*(x-1));
DDEX2=@(x,a) 3/a*3/a*exp(3/a*(x-1));
DDDEX2=@(x,a) 3/a*3/a*3/a*exp(3/a*(x-1));

Es=@(x,a)...
    x(:,1).*x(:,2).^2-...
    x(:,2).^2.*EX1(x(:,1),a)-...
    x(:,1).*EX2(x(:,2),a)+...
    EX1(x(:,1),a).*EX2(x(:,2),a);
D1Es=@(x,a)...
    x(:,2).^2-...
    x(:,2).^2.*DEX1(x(:,1),a)-...
    EX2(x(:,2),a)+...
    DEX1(x(:,1),a).*EX2(x(:,2),a);
D2Es=@(x,a)...
    2*x(:,1).*x(:,2)-...
    2*x(:,2).*EX1(x(:,1),a)-...
    x(:,1).*DEX2(x(:,2),a)+...
    EX1(x(:,1),a).*DEX2(x(:,2),a);
D11Es=@(x,a)...
    -x(:,2).^2.*DDEX1(x(:,1),a)+...
    DDEX1(x(:,1),a).*EX2(x(:,2),a);
D12Es=@(x,a)...
    2*x(:,2)-...
    2*x(:,2).*DEX1(x(:,1),a)-...
    DEX2(x(:,2),a)+...
    DEX1(x(:,1),a).*DEX2(x(:,2),a);
D22Es=@(x,a)...
    2*x(:,1)-...
    2*EX1(x(:,1),a)-...
    x(:,1).*DDEX2(x(:,2),a)+...
    EX1(x(:,1),a).*DDEX2(x(:,2),a);
D111Es=@(x,a)...
    -x(:,2).^2.*DDDEX1(x(:,1),a)+...
    DDDEX1(x(:,1),a).*EX2(x(:,2),a);
D112Es=@(x,a)...
    -2*x(:,2).*DDEX1(x(:,1),a)+...
    DDEX1(x(:,1),a).*DEX2(x(:,2),a);
D122Es=@(x,a)...
    2*ones(size(x,1),1)-...
    2*DEX1(x(:,1),a)-...
    DDEX2(x(:,2),a)+...
    DEX1(x(:,1),a).*DDEX2(x(:,2),a);
D222Es=@(x,a)...
    -x(:,1).*DDDEX2(x(:,2),a)+...
    EX1(x(:,1),a).*DDDEX2(x(:,2),a);

Hv1=@(x,a) -D2Es(x,a);
D1Hv1=@(x,a) -D12Es(x,a);
D2Hv1=@(x,a) -D22Es(x,a); 

Hv2=@(x,a) D1Es(x,a);
D1Hv2=@(x,a) D11Es(x,a);
D2Hv2=@(x,a) D12Es(x,a);

V1=@(x)2*ones(size(x,1),1);
V2=@(x)3*ones(size(x,1),1);

f = @(x,a) a*D11Es(x,a)+a*D22Es(x,a)+V1(x)*D1Es(x,a)+V2(x)*D2Es(x,a)-Es(x,a);
Curlf=@(x,a) [a*D112Es(x,a)+a*D222Es(x,a)+V1(x)*D12Es(x,a)+V2(x)*D22Es(x,a)-D2Es(x,a) ...
                   -a*D111Es(x,a)-a*D122Es(x,a)-V1(x)*D11Es(x,a)-V2(x)*D12Es(x,a)+D1Es(x,a)];

% velocity field
data.V_Handle=@(x,varargin)[V1(x) V2(x)];
% Es
data.Es_Handle=@(x,d,a)Es(x,a);
% grad Hv
data.GRAD_Es_Handle=@(x,d,a)[D1Es(x,a) D2Es(x,a)];
% f
data.Fs_Handle=@(x,d,a)f(x,a);
% Hv
data.Hv_Handle=@(x,d,a)a*[Hv1(x,a) Hv2(x,a)];
% -curl H
data.CURL_Hv_Handle=@(x,d,a)-a*D1Hv2(x,a)+a*D2Hv1(x,a);
% curl f
data.CurlFs_Handle=@(x,d,a)a*Curlf(x,a);

d=data;

return