function d=getData_EvHs2();

% Meshdata
data.boundtype=[-1 -1 -1 -1];
data.Coordinates=[0 0; 1 0; 1 1; 0 1];
data.Elements=[1 2 4;2 3 4];

EX1=@(x,a) exp(2/a*(x-1));
DEX1=@(x,a) 2/a*exp(2/a*(x-1));
DDEX1=@(x,a) 2/a*2/a*exp(2/a*(x-1));
DDDEX1=@(x,a) 2/a*2/a*2/a*exp(2/a*(x-1));
EX2=@(x,a) exp(3/a*(x-1));
DEX2=@(x,a) 3/a*exp(3/a*(x-1));
DDEX2=@(x,a) 3/a*3/a*exp(3/a*(x-1));
DDDEX2=@(x,a) 3/a*3/a*3/a*exp(3/a*(x-1));

Ls=@(x,a)...
    x(:,1).*x(:,2).^2-...
    x(:,2).^2.*EX1(x(:,1),a)-...
    x(:,1).*EX2(x(:,2),a)+...
    EX1(x(:,1),a).*EX2(x(:,2),a);
D1Ls=@(x,a)...
    x(:,2).^2-...
    x(:,2).^2.*DEX1(x(:,1),a)-...
    EX2(x(:,2),a)+...
    DEX1(x(:,1),a).*EX2(x(:,2),a);
D2Ls=@(x,a)...
    2*x(:,1).*x(:,2)-...
    2*x(:,2).*EX1(x(:,1),a)-...
    x(:,1).*DEX2(x(:,2),a)+...
    EX1(x(:,1),a).*DEX2(x(:,2),a);
D11Ls=@(x,a)...
    -x(:,2).^2.*DDEX1(x(:,1),a)+...
    DDEX1(x(:,1),a).*EX2(x(:,2),a);
D12Ls=@(x,a)...
    2*x(:,2)-...
    2*x(:,2).*DEX1(x(:,1),a)-...
    DEX2(x(:,2),a)+...
    DEX1(x(:,1),a).*DEX2(x(:,2),a);
D22Ls=@(x,a)...
    2*x(:,1)-...
    2*EX1(x(:,1),a)-...
    x(:,1).*DDEX2(x(:,2),a)+...
    EX1(x(:,1),a).*DDEX2(x(:,2),a);
D111Ls=@(x,a)...
    -x(:,2).^2.*DDDEX1(x(:,1),a)+...
    DDDEX1(x(:,1),a).*EX2(x(:,2),a);
D112Ls=@(x,a)...
    -2*x(:,2).*DDEX1(x(:,1),a)+...
    DDEX1(x(:,1),a).*DEX2(x(:,2),a);
D122Ls=@(x,a)...
    2*ones(size(x,1),1)-...
    2*DEX1(x(:,1),a)-...
    DDEX2(x(:,2),a)+...
    DEX1(x(:,1),a).*DDEX2(x(:,2),a);
D222Ls=@(x,a)...
    -x(:,1).*DDDEX2(x(:,2),a)+...
    EX1(x(:,1),a).*DDDEX2(x(:,2),a);

Ev1=@(x,a) Ls(x,a);
D1Ev1=@(x,a) D1Ls(x,a);
D2Ev1=@(x,a) D2Ls(x,a); 
D11Ev1=@(x,a) D11Ls(x,a);
D12Ev1=@(x,a) D12Ls(x,a); 
D22Ev1=@(x,a) D22Ls(x,a); 
D111Ev1=@(x,a) D111Ls(x,a);
D112Ev1=@(x,a) D112Ls(x,a);
D122Ev1=@(x,a) D122Ls(x,a); 
D222Ev1=@(x,a) D222Ls(x,a); 

Ev2=@(x,a) Ls(x,a);
D1Ev2=@(x,a) D1Ls(x,a);
D2Ev2=@(x,a) D2Ls(x,a);
D11Ev2=@(x,a) D11Ls(x,a);
D12Ev2=@(x,a) D12Ls(x,a);
D22Ev2=@(x,a) D22Ls(x,a);
D111Ev2=@(x,a) D111Ls(x,a);
D112Ev2=@(x,a) D112Ls(x,a);
D122Ev2=@(x,a) D122Ls(x,a);
D222Ev2=@(x,a) D222Ls(x,a);

V1=@(x)2*ones(size(x,1),1);
V2=@(x)3*ones(size(x,1),1);

fv=@(x,a) [-a*D22Ev1(x,a)+a*D12Ev2(x,a)+V2(x)*(D1Ev2(x,a)-D2Ev1(x,a))+Ev1(x,a) ...
                 a*D12Ev1(x,a)-a*D11Ev2(x,a)-V1(x)*(D1Ev2(x,a)-D2Ev1(x,a))+Ev2(x,a)];

Curlfv = @(x,a) a*D112Ev1(x,a)-a*D111Ev2(x,a)-V1(x)*(D11Ev2(x,a)-D12Ev1(x,a))+D1Ev2(x,a)-...
    (-a*D222Ev1(x,a)+a*D122Ev2(x,a)+V2(x)*(D12Ev2(x,a)-D22Ev1(x,a))+D2Ev1(x,a));

% velocity field
data.V_Handle=@(x,varargin)[V1(x) V2(x)];
% Hs
%  data.Hs_Handle=@(x,d,a)[;
% grad Hs
data.GRAD_Hs_Handle=@(x,d,a)[D1Hs(x,a) D2Hs(x,a)];
% curl fv
data.CurlFv_Handle=@(x,d,a)Curlfv(x,a);
% Ev
data.Ev_Handle=@(x,d,a)[Ev1(x,a) Ev2(x,a)];
% -curl Ev
data.CURL_Ev_Handle=@(x,d,a)-D1Ev2(x,a)+D2Ev1(x,a);
% fv
data.Fv_Handle=@(x,d,a)fv(x,a);

d=data;

return